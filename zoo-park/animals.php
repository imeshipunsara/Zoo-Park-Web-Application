        <h2>Diet</h2>
        <p>Although pandas are classified as carnivores, they are primarily herbivorous, with over 99% of their diet consisting of bamboo. In the wild, pandas occasionally eat other vegetation, wild tubers, or even small animals, though this makes up a minor part of their diet.
            Pandas need to consume around 12-38 kilograms (26-84 lbs) of bamboo each day to meet their energy needs</p>
        <h2>Physical Characteristics</h2>
        <ul>
            Size: Adult pandas weigh between 100-150 kilograms (220-330 lbs), with males being larger than females. <br>
            Coloration: Their black-and-white coloration helps them blend into their snowy and forested environments, acting as camouflage <br>
            Teeth and Jaws: Pandas have strong molars and powerful jaw muscles, enabling them to crush bamboo stalks effectively. <br>
        </ul>
        <h2>Behavior</h2>
        <ul>
            Solitary Nature: Pandas are mostly solitary creatures, with each adult having a defined territory. They only come together during the mating season. <br>
            Sleeping: Pandas spend a significant portion of their day (10-16 hours) eating and resting. <br>
            Communication: While relatively quiet animals, pandas communicate through vocalizations such as bleats, growls, and honks, and through scent markings. <br>
